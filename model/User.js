const { Schema } = require("mongoose");

const UserSchema = new Schema({
  username: {
    type: String,
    unique: true,
    required: true,
    trim: true,
  },
  email: {
    type: String,
    unique: true,
    required: true,
    validate : {
        validator : function(email) {
            return /^([\w-\.]+@([\w-]+\.)+[\w-]{2,7})?$/.test(email);
        },
        message : "Please enter a valid email"
    }
  },
  thoughts: [
    {
      type: Schema.Types.ObjectId,
      ref: "Thought",
    },
  ],
  friends: [
    {
      type: Schema.Types.ObjectId,
      ref: "User",
    },
  ],
});

